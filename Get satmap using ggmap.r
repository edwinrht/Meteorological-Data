map + geom_point(data = spatial, aes(x = Lon, y = Lat), color="red", size=2, alpha=0.5)
map + stat_density2d(aes(x = Lon, y = Lat, fill = ..level.., alpha = ..level..*2), size = 2, bins = 5, data = spatial, geom = "polygon") + scale_fill_gradient(low = "black", high = "red")
