library(ggmap)
library(sp)
spatial=read.csv("Spatial.csv",T)
coords < - cbind(Longitude = as.numeric(as.character(spatial$Lon)), Latitude = as.numeric(as.character(spatial$Lat)))
yph< - SpatialPointsDataFrame(coords, spatial[, (13:14)], proj4string = CRS("+init=epsg:4326"))
plot(yph, pch = ".", col = "darkred")
map< - qmap('Valparai', zoom = 12, maptype = 'satellite')
#map< - qmap('Valparai', zoom = 12, maptype = 'trerrain')
